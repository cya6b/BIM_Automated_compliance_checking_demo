<template>
  <div id="app">
    <router-view/>
  </div>
</template>

<script>
export default {
  name: 'App',
  created () {
    // store数据页面刷新丢失问题
    // 在页面加载时读取sessionStorage里的状态信息
    if (sessionStorage.getItem('store')) {
      this.$store.replaceState(Object.assign({}, this.$store.state, JSON.parse(sessionStorage.getItem("store"))))
    }

    // 替换页面页签title内容
    document.title = this.SYS_GLOBAL[this.SYS_GLOBAL.sysDicCode].title
  }
}
</script>

